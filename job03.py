âge = int(input("Bonjour, quel âge as-tu?: ")) #Je créer une variable en input avec la class int afin d'indiquer que ma valeur sera numérique.
print(âge)                                     #impréssion de la valeur saisi.

if âge < 18:                                   #J'indique que si l'âge est inférieur.
        print("Vous êtes mineur")              #"Vous êtes mineur" apparaitra sur le terminal.
else:                                          ##j'indique que si l'âge est supérieur
        print("Vous êtes majeur")              #"Vous êtes majeur" apparaitra sur le terminal.
